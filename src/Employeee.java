
public class Employeee {

	int id;
	double salary;
	
	public Employeee(int empId, double sal) {
		id = empId;
		salary = sal;
	}
	
	public void setId(int empId) {
		id = empId;
	}
	
	public int getId() {
		return id;
	}

	public void setSalary(double sal) {
		salary = sal;
	}
	
	public double getSalary() {
		return salary;
	}
	
	
	
	
}
