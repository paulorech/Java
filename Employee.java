

public class Employee {
	
		int empNum;
		double salary;
		
		public Employee(int e, double s) {
			empNum = e;
			salary = s;
		}
		
		public void setEmpNum(int e) {
			empNum = e;
		}
		
		public int getEmpNum() {
			return empNum;
		}
		
		public void setSalary(double s) {
			salary = s;
		}
		
		public double getSalary() {
			return salary;
		}
	}
	

